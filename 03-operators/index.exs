# Suma
IO.puts( 1 + 2)

# Resta
IO.puts( 3 - 1 )

# Division
IO.puts( 7 / 2 )

# Multiplicacion
IO.puts( 8 * 3 )

# div function solo se define para enteros
IO.puts( div(1,2) )

# Residuo rem
IO.puts( rem(1,2) )

# Concatenar cadenas
s1 = "Hello "
s2 = "World"
s3 = s1 <> s2
IO.puts( s3 )

# ********************
# Operadores booleanos
# ********************

comp1 = true and false
IO.puts( comp1 )

comp2 = false or true
IO.puts( comp2 )

comp3 = not(false)
IO.puts( comp3 )

# Operador or
comp4 = true || false
IO.puts( comp4 )

# Operador and
comp5 = true && false
IO.puts( comp5 )

# Operador not
comp6 = !true
IO.puts( comp6 )

# **********************
# Operadores comparacion
# **********************

# Igual
IO.puts( 1 == 1 )

# Diferente
IO.puts( 1 != 2 )

# Mayor
IO.puts( 1 > 2 )

# Menos
IO.puts( 1 < 2 )

# Mayor o igual
IO.puts( 1 >= 2 )

# Menor o igual
IO.puts( 1 <= 2 )

